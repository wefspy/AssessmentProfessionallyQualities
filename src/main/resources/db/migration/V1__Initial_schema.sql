SELECT setval('tasks_seq', (SELECT MAX(id) FROM tasks));


WITH rated_tasks AS (
    SELECT t.id as task_id,
           t.assignee_member_id,
           tm.user_id,
           t.status
    FROM tasks t
             JOIN team_members tm ON t.assignee_member_id = tm.id
    WHERE t.status = 'RATED'
),
     user_skills_to_evaluate AS (
         SELECT rt.task_id,
                us.id as user_skill_id,
                us.rating as target_rating,
                us.user_id
         FROM rated_tasks rt
                  JOIN users_skills us ON rt.user_id = us.user_id
     )
INSERT INTO evaluations (task_id, user_skill_id, evaluation, feedback)
SELECT
    use.task_id,
    use.user_skill_id,
    use.target_rating as evaluation,
    'Auto-generated evaluation to match existing rating' as feedback
FROM user_skills_to_evaluate use
WHERE NOT EXISTS (
    SELECT 1
    FROM evaluations e
    WHERE e.task_id = use.task_id
      AND e.user_skill_id = use.user_skill_id
);

SELECT setval('evaluations_seq', (SELECT COALESCE(MAX(id), 0) FROM evaluations), true);


