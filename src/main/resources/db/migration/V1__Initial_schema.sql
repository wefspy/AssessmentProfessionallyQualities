-- Sequences
CREATE SEQUENCE IF NOT EXISTS users_seq START WITH 1 INCREMENT BY 5;
CREATE SEQUENCE IF NOT EXISTS roles_seq START WITH 1 INCREMENT BY 5;
CREATE SEQUENCE IF NOT EXISTS users_roles_seq START WITH 1 INCREMENT BY 10;
CREATE SEQUENCE IF NOT EXISTS skill_categories_seq START WITH 1 INCREMENT BY 10;
CREATE SEQUENCE IF NOT EXISTS skills_seq START WITH 1 INCREMENT BY 10;
CREATE SEQUENCE IF NOT EXISTS users_skills_seq START WITH 1 INCREMENT BY 10;
CREATE SEQUENCE IF NOT EXISTS teams_seq START WITH 1 INCREMENT BY 10;
CREATE SEQUENCE IF NOT EXISTS team_members_seq START WITH 1 INCREMENT BY 10;
CREATE SEQUENCE IF NOT EXISTS tasks_seq START WITH 1 INCREMENT BY 10;
CREATE SEQUENCE IF NOT EXISTS evaluations_seq START WITH 1 INCREMENT BY 25;
CREATE SEQUENCE IF NOT EXISTS task_evaluated_skills_seq START WITH 1 INCREMENT BY 1;

-- Custom Types
DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'task_status') THEN
        CREATE TYPE task_status AS ENUM ('UNAVAILABLE', 'IN_WAITING', 'RATED', 'CANCELLED');
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'education') THEN
        CREATE TYPE education AS ENUM ('BACCALAUREATE', 'MAGISTRATE', 'POSTGRADUATE', 'SPECIALTY');
